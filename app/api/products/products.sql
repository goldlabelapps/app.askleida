create table public.products (
  product_id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  title text null,
  description text null,
  brand text null,
  routine_step integer null,
  treat_sublabel text null,
  distribution_type text null,
  website_url text null,
  claude_description text null,
  how_to_apply text null,
  active_ingredients text null,
  contraindications text null,
  is_pregnancy_safe boolean null,
  is_breastfeeding_safe boolean null,
  skin_type_tags jsonb null,
  concern_tags jsonb null,
  image_url text null,
  is_verified boolean null,
  is_seeded boolean null,
  price numeric null,
  slug text null,
  constraint products_pkey primary key (product_id),
  constraint products_slug_key unique (slug)
) TABLESPACE pg_default;